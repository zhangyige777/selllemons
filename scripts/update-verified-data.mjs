function hashText(text) {
  return crypto.createHash('sha256').update(text).digest('hex')
}

function normalizeHtml(html) {
  return html
    .replace(/<script[\s\S]*?<\/script>/gi, ' ')
    .replace(/<style[\s\S]*?<\/style>/gi, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

function todayLabel() {
  return new Intl.DateTimeFormat('en-US', {
    month: 'long',
    day: 'numeric',
    year: 'numeric',
    timeZone: 'UTC',
  }).format(new Date())
}

async function readPreviousHashes() {
  try {
    const content = await fs.readFile(watchFile, 'utf8')
    const matches = [...content.matchAll(/"url": "([^"]+)"[\s\S]*?"hash": "([a-f0-9]{64})"/g)]
    return new Map(matches.map((match) => [match[1], match[2]]))
  } catch {
    return new Map()
  }
}

async function fetchSource(source, previousHashes) {
  const startedAt = Date.now()
  try {
    const response = await fetch(source.url, {
      headers: {
        'user-agent': 'sell-lemons-data-watch/1.0',
      },
    })
    const html = await response.text()
    const normalized = normalizeHtml(html)
    const hash = hashText(normalized)

    return {
      ...source,
      ok: response.ok,
      status: response.status,
      checkedAt: new Date().toISOString(),
      responseMs: Date.now() - startedAt,
      contentLength: normalized.length,
      hash,
      changed: previousHashes.get(source.url) !== hash,
    }
  } catch (error) {
    return {
      ...source,
      ok: false,
      status: 0,
      checkedAt: new Date().toISOString(),
      responseMs: Date.now() - startedAt,
      contentLength: 0,
      hash: '',
      changed: true,
      error: error instanceof Error ? error.message : String(error),
    }
  }
}

async function fetchJson(url) {
  const response = await fetch(url, {
    headers: {
      'user-agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/125.0 Safari/537.36',
      accept: 'application/json',
      'accept-language': 'en-US,en;q=0.9',
    },
  })

  if (!response.ok) {
    return fetchJsonWithCurl(url, `HTTP ${response.status}`)
  }

  return response.json()
}

async function fetchJsonWithCurl(url, originalError) {
  const { stdout } = await execFileAsync('curl', [
    '-L',
    '--fail',
    '--silent',
    '--show-error',
    '-H',
    'User-Agent: Mozilla/5.0',
    '-H',
    'Accept: application/json',
    url,
  ], { maxBuffer: 1024 * 1024 * 10 })

  try {
    return JSON.parse(stdout)
  } catch (error) {
    throw new Error(`${originalError}; curl returned invalid JSON for ${url}: ${error instanceof Error ? error.message : String(error)}`)
  }
}

