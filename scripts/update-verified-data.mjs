import crypto from 'node:crypto'
import fs from 'node:fs/promises'
import path from 'node:path'

const root = process.cwd()
const watchFile = path.join(root, 'src', 'data', 'source-watch.ts')
const codesFile = path.join(root, 'src', 'data', 'codes.ts')

const sources = [
  {
    label: 'Roblox game page',
    url: 'https://www.roblox.com/games/79268393072444',
    trust: 'official',
    use: 'Game description, update notes, codes, badges, keys, and gamepasses when visible.',
  },
  {
    label: 'Google Trends manual lead',
    url: 'https://trends.google.com/trends/explore?q=Sell%20Lemons',
    trust: 'lead',
    use: 'Search demand only; never a source for game facts.',
  },
]

function hashText(text) {
  return crypto.createHash('sha256').update(text).digest('hex')
}

function normalizeHtml(html) {
  return html
    .replace(/<script[\s\S]*?<\/script>/gi, ' ')
    .replace(/<style[\s\S]*?<\/style>/gi, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

function todayLabel() {
  return new Intl.DateTimeFormat('en-US', {
    month: 'long',
    day: 'numeric',
    year: 'numeric',
    timeZone: 'UTC',
  }).format(new Date())
}

async function readPreviousHashes() {
  try {
    const content = await fs.readFile(watchFile, 'utf8')
    const matches = [...content.matchAll(/"url": "([^"]+)"[\s\S]*?"hash": "([a-f0-9]{64})"/g)]
    return new Map(matches.map((match) => [match[1], match[2]]))
  } catch {
    return new Map()
  }
}

async function fetchSource(source, previousHashes) {
  const startedAt = Date.now()
  try {
    const response = await fetch(source.url, {
      headers: {
        'user-agent': 'sell-lemons-data-watch/1.0',
      },
    })
    const html = await response.text()
    const normalized = normalizeHtml(html)
    const hash = hashText(normalized)

    return {
      ...source,
      ok: response.ok,
      status: response.status,
      checkedAt: new Date().toISOString(),
      responseMs: Date.now() - startedAt,
      contentLength: normalized.length,
      hash,
      changed: previousHashes.get(source.url) !== hash,
    }
  } catch (error) {
    return {
      ...source,
      ok: false,
      status: 0,
      checkedAt: new Date().toISOString(),
      responseMs: Date.now() - startedAt,
      contentLength: 0,
      hash: '',
      changed: true,
      error: error instanceof Error ? error.message : String(error),
    }
  }
}

function toTs(results) {
  return `// Generated by scripts/update-verified-data.mjs
// This file records source checks. It does not turn unverified source changes into game facts.

export const sourceWatch = ${JSON.stringify(results, null, 2)} as const

export function getLastSourceCheckDate(): string {
  return '${todayLabel()}'
}
`
}

async function updateCodesLastChecked() {
  const content = await fs.readFile(codesFile, 'utf8')
  const updated = content.replace(
    /export function getLastCheckedDate\(\): string \{\s*return '[^']*'\s*\}/,
    `export function getLastCheckedDate(): string {\n  return '${todayLabel()}'\n}`,
  )
  await fs.writeFile(codesFile, updated, 'utf8')
}

const previousHashes = await readPreviousHashes()
const results = []
for (const source of sources) {
  results.push(await fetchSource(source, previousHashes))
}

await fs.writeFile(watchFile, toTs(results), 'utf8')
await updateCodesLastChecked()

const changed = results.filter((result) => result.changed).map((result) => result.label)
console.log(`Checked ${results.length} sources.`)
console.log(changed.length ? `Changed sources: ${changed.join(', ')}` : 'No source changes detected.')
