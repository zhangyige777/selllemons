async function updateCodesLastChecked() {
  const content = await fs.readFile(codesFile, 'utf8')
  const updated = content.replace(
    /export function getLastCheckedDate\(\): string \{\s*return '[^']*'\s*\}/,
    `export function getLastCheckedDate(): string {\n  return '${todayLabel()}'\n}`,
  )
  await fs.writeFile(codesFile, updated, 'utf8')
}

async function updatePageStatuses(unlocks) {
  let content = await fs.readFile(pagesFile, 'utf8')
  const unlockMap = new Map(unlocks.map((unlock) => [unlock.slug, unlock]))

  content = content.replace(
    /(slug: '([^']+)',[\s\S]*?dataStatus: ')(confirmed|possible|unconfirmed)(',)/g,
    (match, prefix, slug, currentStatus, suffix) => {
      const unlock = unlockMap.get(slug)
      if (!unlock || unlock.status !== 'confirmed') return match
      if (currentStatus === 'confirmed') return match
      return `${prefix}confirmed${suffix}`
    },
  )

  await fs.writeFile(pagesFile, content, 'utf8')
}

